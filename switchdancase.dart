void main(List<String> args) {
  var nilai = 'D';
  switch (nilai) {
    case 'A':
      print('baik');
      break;
    case 'B':
      print('cukup');
      break;
    case 'C':
      print('kurang cukup');
      break;
    case 'D':
      print('remidi');
      break;
    default:
      print('tidak ada nilai');
  }
}
